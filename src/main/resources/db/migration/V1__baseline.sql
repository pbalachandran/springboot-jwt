CREATE TABLE USERS
(
    ID       BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR(20),
    EMAIL    VARCHAR(50),
    PASSWORD VARCHAR(120)
);

CREATE TABLE ROLES
(
    ID      BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME    VARCHAR(20),
    USER_ID BIGINT
        CONSTRAINT FK_ROLES_USERS REFERENCES USERS

);
